// Negácie a zosilňovače musia zostať zachované,
// inak by sa zhoršila sentimentová analýza.
const RESERVED_WORDS = new Set([
  "nie",
  "nikdy",
  "ziadny",
  "ziadna",
  "ziadne",
  "ani",
  "velmi",
  "mimoriadne",
  "extremne",
  "naozaj",
  "fakt",
  "prilis",
  "moc",
  "dost",
]);

export function isStopWord(token: string) {
  if (!token) return false;
  if (RESERVED_WORDS.has(token)) return false;
  return SLOVAK_STOP_WORDS.has(token);
}

export function removeStopWords(tokens: string[]) {
  return tokens.filter((token) => !isStopWord(token));
}